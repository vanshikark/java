public class grade {
    public static void main(String[] args){
        int marks=85;
        if(marks>=90)
        {
            System.out.println("Grade is A");
        }
        else if(marks>=85)
        {
            System.out.println("Grade is B");
        }
        else if(marks>=60)
        {
            System.out.println("Grade is C");
        }
        else
        {
            System.out.println("Fail");
        }
    }
    
}
